#include <iostream>
#include <cmath>
#include <iomanip>

using namespace std;

int main() {
    double A, B, C, D, x1, x2;
    
    // Стан П: Введення даних
    cout << "Введіть коефіцієнти A, B, C: ";
    if (!(cin >> A >> B >> C)) {
        cout << "Помилка введення!" << endl;
        return 1;
    }

    // Стан 1: Аналіз коефіцієнта А
    if (A != 0) { 
        // Квадратне рівняння (Перехід до Стан 2)
        if (B != 0) {
            // Стан 3: Розрахунок дискримінанту
            D = B * B - 4 * A * C;
            if (D > 0) {
                // Стан 4
                x1 = (-B + sqrt(D)) / (2 * A);
                x2 = (-B - sqrt(D)) / (2 * A);
                cout << "Рівняння квадратне. D > 0. Корені: x1 = " << x1 << ", x2 = " << x2 << endl;
            } else if (D == 0) {
                // Стан 9
                x1 = -B / (2 * A);
                cout << "Рівняння квадратне. D = 0. Корінь: x = " << x1 << endl;
            } else {
                // Стан 10
                cout << "Рівняння квадратне. D < 0. Дійсних коренів немає." << endl;
            }
        } else {
            // Стан 2 -> Стан 7/8 (B = 0, неповне квадратне)
            double val = -C / A;
            if (val >= 0) {
                // Стан 7
                x1 = sqrt(val);
                x2 = -sqrt(val);
                cout << "Неповне квадратне (B=0). Корені: x = ±" << x1 << endl;
            } else {
                // Стан 8
                cout << "Неповне квадратне (B=0). Коренів немає (від'ємне число під коренем)." << endl;
            }
        }
    } else {
        // Стан 6: Лінійне рівняння (A = 0)
        if (B != 0) {
            x1 = -C / B;
            cout << "Рівняння лінійне (A=0). Корінь: x = " << x1 << endl;
        } else {
            // Стан 5: A = 0, B = 0
            if (C == 0) {
                cout << "Рівняння виду 0=0. Безліч розв'язків." << endl;
            } else {
                cout << "Суперечність " << C << "=0. Розв'язків немає." << endl;
            }
        }
    }

    return 0;
}